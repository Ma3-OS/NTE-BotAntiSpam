import os
from dotenv import load_dotenv

# โหลดค่าจากไฟล์ .env
load_dotenv()
OWNER_ID = os.getenv('OWNER_ID')

def get_public_warning(user_mention):
    """ข้อความหน้าบ้าน: ส่งเข้าช่องแชทที่เกิดเหตุ"""
    return (
        f"🛡️ **นี้คือการแจ้งเตือนลบรูปภาพสแปม** {user_mention}\n"
        f"> ระบบตรวจพบว่ารูปภาพที่คุณเพิ่งส่งมามีเนื้อหาเข้าข่ายสแปม โฆษณา หรือลิงก์อันตราย\n"
        f"> เพื่อความปลอดภัยของเพื่อนๆ ในเซิร์ฟเวอร์ ข้อความเมื่อกี้จึงถูกลบทิ้ง และระบบได้ Timeout ชั่วคราว\n"
        f"> หากคิดว่าบอทเข้าใจผิดหรือแก้ไขรหัสเสร็จแล้วสามารถทักไปหา <@{OWNER_ID}> ได้เลย"
    )

